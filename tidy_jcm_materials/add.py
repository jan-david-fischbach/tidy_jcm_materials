def add(a: float, b: float) -> float:
    """The sum of two numbers.

    Parameters
    ----------
    a : float
        One summand
    b : float
        The other summand

    Returns
    -------
    float
        the sum of `a` and `b`

    Notes
    -----
        this is obviously ridiculous for such simple functions
    """
    return a + b
